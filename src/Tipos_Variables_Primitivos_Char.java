public class Tipos_Variables_Primitivos_Char {

    public static void main(String[] args) {


        System.out.println(" Cantidad de Bits var tipo char  -->>  "+Character.SIZE);
        System.out.println(" Cantidad de Bytes var tipo char  -->>  "+Character.BYTES);
        System.out.println(" Valor minimo en var tipo char  -->>  "+Character.MIN_VALUE);
        System.out.println(" Valor minimo en var tipo char  -->>  "+Character.MAX_VALUE);
        System.out.println("");

        char charVar='\u0021';
        System.out.println("charVar = " + charVar);

        char charVar2=33;    /*  En decimal  */
        System.out.println("charVar = " + charVar2);

        char charVar3='!';    /*  En decimal  */
        System.out.println("charVar = " + charVar3);


    }
}

